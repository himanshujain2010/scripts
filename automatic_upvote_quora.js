/* script that i used to automatically upvote all the answer of my friend */
var add_upvote = document.getElementsByClassName("add_upvote");
for(var i =0;i<add_upvote.length;i++){
   add_upvote[i].click();
}

/* similary can be used to remove all your upvote */ 
var remove_upvote = document.getElementsByClassName("remove_upvote");
for(var i =0;i<remove_upvote.length;i++){
   remove_upvote[i].click();
}
